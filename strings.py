print(name.upper()) #SASUNI1234567890 (uppercase)
print(name.lower()) #sasuni1234567890 (lowercase)
print(name.replace("Sasuni", "S") ) #S1234567890 (replace substring)
print(name.split("i")) #['Sasun', '1234567890'] (split string)
print("Sasuni" in name) #True (check substring) 
print("Sasuni" not in name) #False (check substring)
print(name.index("Sasuni")) #0 (index of substring)
print(name.count("a")) #1 (count occurrences of substring)
print(name.startswith("Sasuni")) #True (check start substring)
print(name.endswith("7890")) #True (check end substring)
print(name.isalnum()) #True (check if alphanumeric)
print(name.isalpha()) #False (check if alphabetic)
print(name.isdigit()) #False (check if digit)
print(name.isspace()) #False (check if whitespace)
print(name.find("Sasuni")) #0 (find substring index)
print(name.find("xyz")) #-1 (substring not found)
print(name.center(20, "*")) #*******Sasuni1234567890******* (centered with padding)
print(name.ljust(20, "-")) #Sasuni1234567890---- (left justified with padding)
print(name.rjust(20, "-")) #----Sasuni1234567890 (right justified with padding)
print(name.zfill(20)) #00000000Sasuni1234567890 (zero filled)
print(name.title()) #Sasuni1234567890 (title case)  

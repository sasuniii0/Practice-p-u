name = "Sasuni1234567890"

print(name[0:6])  #Sasuni
print(name[6:])   #1234567890
print(name[:6])   #Sasuni
print(name[-4:])  #7890
print(name[:-4])  #Sasuni123456
print(name[::2])  #Ssn13480 (every second character)
print(name[::-1]) #0987654321inusaS (reversed string)

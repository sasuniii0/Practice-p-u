print(name.swapcase()) #sASUNI1234567890 (swap case)    
print(name.partition("123")) #('Sasuni', '123', '4567890') (partition string)
print(name.rpartition("123")) #('Sasuni', '123', '4567890') (right partition string)
print(name.encode()) #b'Sasuni1234567890' (encoded string)
